        private void button1_Click(object sender, EventArgs e)
        {
            click.Play();

            if (button1.Text == "START")
            {
                // Start the challenge
                button1.Text = "DONE";
                button1.Enabled = false;
                EssayrichTextBox1.Enabled = false; // Enable rich text box for typing
                textBox2.Enabled = false;
                button4.Enabled = false;
                pictureBox11.Enabled = false;
                isEnterKeyDisabled = true;

                soundPlayer.Play();

                // Delay the start of the timer and progress bar
                Timer audioTimer = new Timer();
                audioTimer.Interval = 4000; // Adjusted delay for audio
                audioTimer.Tick += (s, args) =>
                {
                    audioTimer.Stop();
                    audioTimer.Dispose();
                    StartChallenge();
                };
                audioTimer.Start();
            }
            else if (button1.Text == "DONE")
            {
                // Validate content length
                if (EssayrichTextBox1.Text.Length <= 300)
                {
                    // Error message for insufficient content
                    MessageBox.Show("Your gratitude essay must be at least 300 characters long to complete the challenge.", "Validation Error", MessageBoxButtons.OK, MessageBoxIcon.Error);
                    return;
                }

                // Challenge completed successfully
                timer.Stop();
                success.Play();
                progressBar1.Value = progressBar1.Maximum; // Set progress bar to max
                textBox2.Text = TimeSpan.FromSeconds(progressDuration - timeRemaining).ToString("hh\\:mm\\:ss"); // Update final time

                // Save the essay content
                SaveEssayContent(EssayrichTextBox1.Text);

                SaveChallengeDataSuccess(Login.CurrentUsername, "Completed", textBox2.Text, "Gratitude (Medium)");

                MessageBox.Show("Challenge Success. Returning to the homepage.", "Success", MessageBoxButtons.OK, MessageBoxIcon.Information);
                TransitionToHomePage();
            }
        }

