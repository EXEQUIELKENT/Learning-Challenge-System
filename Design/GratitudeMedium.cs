        private void SaveEssayContent(string essayText)
        {
            try
            {
                DialogResult result = MessageBox.Show("Do you want to save your gratitude essay?", "Save File", MessageBoxButtons.YesNo, MessageBoxIcon.Question);

                if (result == DialogResult.Yes)
                {
                    // Initialize SaveFileDialog
                    SaveFileDialog saveFileDialog = new SaveFileDialog
                    {
                        Filter = "Text Files (*.txt)|*.txt", // Set file type filter
                    };

                    // Show the SaveFileDialog
                    if (saveFileDialog.ShowDialog() == DialogResult.OK)
                    {
                        string filePath = saveFileDialog.FileName;

                        // Check if the file already exists
                        int fileCount = 1;
                        while (File.Exists(filePath))
                        {
                            // If the file exists, create a new name with an incremented number
                            filePath = Path.Combine(Path.GetDirectoryName(filePath),
                                Path.GetFileNameWithoutExtension(filePath) + $" ({fileCount})" + Path.GetExtension(filePath));
                            fileCount++;
                        }

                        // Save the essay content to the file
                        File.WriteAllText(filePath, essayText);
                        MessageBox.Show($"Your essay has been saved as a text file: {filePath}", "File Saved", MessageBoxButtons.OK, MessageBoxIcon.Information);
                    }
                }  
            }
            catch (Exception)
            {
                // If saving as .txt fails, save as an image
                try
                {
                    Bitmap bitmap = new Bitmap(EssayrichTextBox1.Width, EssayrichTextBox1.Height);
                    EssayrichTextBox1.DrawToBitmap(bitmap, new Rectangle(0, 0, EssayrichTextBox1.Width, EssayrichTextBox1.Height));
                    string imagePath = Path.Combine(Environment.GetFolderPath(Environment.SpecialFolder.MyPictures), "Essay.png");
                    bitmap.Save(imagePath);
                    MessageBox.Show($"Your essay has been saved as an image: {imagePath}", "File Saved as Image", MessageBoxButtons.OK, MessageBoxIcon.Information);
                }
                catch (Exception ex)
                {
                    MessageBox.Show($"Failed to save your essay. Error: {ex.Message}", "Save Error", MessageBoxButtons.OK, MessageBoxIcon.Error);
                }
            }
        }
